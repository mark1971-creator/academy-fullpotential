// Optional: merges pulled env files into .env.local for local development.
// Legacy Vercel workflow: vercel env pull .env.preview.pull && node scripts/prepare-local-env.mjs
// Preferred: copy .env.example to .env.local and fill in values.

import { readFileSync, writeFileSync, existsSync, unlinkSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const envPath = join(root, ".env.local");
const previewPullPath = join(root, ".env.preview.pull");
const productionPullPath = join(root, ".env.production.pull");

function parseEnvFile(path) {
  if (!existsSync(path)) return new Map();
  const map = new Map();
  for (const line of readFileSync(path, "utf8").split(/\r?\n/)) {
    if (!line.trim() || line.trim().startsWith("#")) continue;
    const eq = line.indexOf("=");
    if (eq === -1) continue;
    const key = line.slice(0, eq).trim();
    let value = line.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    if (value) map.set(key, value);
  }
  return map;
}

function mergeEnv(target, source) {
  for (const [key, value] of source) {
    if (value) target.set(key, value);
  }
}

function serializeEnv(map, header) {
  const lines = [header, ""];
  for (const [key, value] of map) {
    lines.push(`${key}="${value}"`);
  }
  lines.push("");
  return lines.join("\n");
}

const preview = parseEnvFile(previewPullPath);
const production = parseEnvFile(productionPullPath);
const merged = new Map();

mergeEnv(merged, preview);
mergeEnv(merged, production);

merged.set("NEXT_PUBLIC_APP_URL", "http://localhost:3000");

const LOCAL_REDIRECT_KEYS = [
  "NEXT_PUBLIC_CLERK_SIGN_IN_FORCE_REDIRECT_URL",
  "NEXT_PUBLIC_CLERK_SIGN_UP_FORCE_REDIRECT_URL",
  "NEXT_PUBLIC_CLERK_AFTER_SIGN_IN_URL",
  "NEXT_PUBLIC_CLERK_AFTER_SIGN_UP_URL",
];

for (const key of LOCAL_REDIRECT_KEYS) {
  if (merged.has(key)) merged.set(key, "/my-courses");
}

if (!merged.has("STRIPE_SECRET_KEY")) merged.set("STRIPE_SECRET_KEY", "");
if (!merged.has("STRIPE_WEBHOOK_SECRET")) merged.set("STRIPE_WEBHOOK_SECRET", "");

const STRIP_PREFIXES = ["VERCEL_GIT_"];
const STRIP_KEYS = new Set([
  "VERCEL",
  "VERCEL_ENV",
  "VERCEL_URL",
  "VERCEL_TARGET_ENV",
  "VERCEL_OIDC_TOKEN",
  "NX_DAEMON",
  "TURBO_CACHE",
  "TURBO_DOWNLOAD_LOCAL_ENABLED",
  "TURBO_REMOTE_ONLY",
  "TURBO_RUN_SUMMARY",
]);

for (const key of [...merged.keys()]) {
  if (STRIP_KEYS.has(key) || STRIP_PREFIXES.some((p) => key.startsWith(p))) {
    merged.delete(key);
  }
}

const ORDER = [
  "NEXT_PUBLIC_CLERK_PUBLISHABLE_KEY",
  "CLERK_SECRET_KEY",
  "NEXT_PUBLIC_CLERK_SIGN_IN_URL",
  "NEXT_PUBLIC_CLERK_SIGN_UP_URL",
  "NEXT_PUBLIC_CLERK_SIGN_IN_FORCE_REDIRECT_URL",
  "NEXT_PUBLIC_CLERK_SIGN_UP_FORCE_REDIRECT_URL",
  "NEXT_PUBLIC_CLERK_AFTER_SIGN_IN_URL",
  "NEXT_PUBLIC_CLERK_AFTER_SIGN_UP_URL",
  "NEXT_PUBLIC_SUPABASE_URL",
  "NEXT_PUBLIC_SUPABASE_ANON_KEY",
  "SUPABASE_SERVICE_ROLE_KEY",
  "STRIPE_SECRET_KEY",
  "STRIPE_WEBHOOK_SECRET",
  "NEXT_PUBLIC_APP_URL",
];

const ordered = new Map();
for (const key of ORDER) {
  if (merged.has(key)) ordered.set(key, merged.get(key));
}
for (const [key, value] of merged) {
  if (!ordered.has(key)) ordered.set(key, value);
}

writeFileSync(
  envPath,
  serializeEnv(ordered, "# Local development — generated by scripts/prepare-local-env.mjs"),
  "utf8",
);

for (const path of [previewPullPath, productionPullPath]) {
  if (existsSync(path)) unlinkSync(path);
}

const missing = [];
for (const key of [
  "NEXT_PUBLIC_CLERK_PUBLISHABLE_KEY",
  "CLERK_SECRET_KEY",
  "NEXT_PUBLIC_SUPABASE_URL",
  "NEXT_PUBLIC_SUPABASE_ANON_KEY",
  "SUPABASE_SERVICE_ROLE_KEY",
]) {
  if (!ordered.get(key)) missing.push(key);
}
if (!ordered.get("STRIPE_SECRET_KEY")) {
  missing.push("STRIPE_SECRET_KEY (Stripe Dashboard → Developers → API keys)");
}
if (!ordered.get("STRIPE_WEBHOOK_SECRET")) {
  missing.push("STRIPE_WEBHOOK_SECRET (stripe listen --forward-to localhost:3000/api/webhooks/stripe)");
}

console.log("Wrote .env.local");
console.log("Keys:", [...ordered.keys()].join(", "));
if (missing.length) {
  console.log("\nStill needed:");
  missing.forEach((m) => console.log("  -", m));
} else {
  console.log("\nCore keys present. Add Stripe keys for payment testing.");
}
